
export const generateMinutesSeconds = (minutes: number) => minutes * 60;
export const generateMinutesMilliSeconds = (minutes: number) => minutes * 60 * 1000;


export const generateDaysSeconds = (days: number) => days * 24 * 60 * 60;
export const generateDaysMilliSeconds = (days: number) => days * 24* 60 * 60 * 1000;
